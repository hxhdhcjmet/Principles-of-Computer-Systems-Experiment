static inline uint8_t sm4Sbox(uint8_t in) {
    return SboxTable[in >> 4][in & 0x0F];
}

static inline uint32_t sm4L(uint32_t in) {
    return in ^ (in << 2) ^ (in << 10) ^ (in << 18) ^ (in << 24) ^
           (in >> 6) ^ (in >> 22) ^ (in >> 14) ^ (in >> 8);
}

static inline uint32_t sm4T(uint32_t in) {
    uint32_t out;
    uint8_t *p = (uint8_t*)&out;
    p[0] = sm4Sbox((in >> 24) & 0xFF);
    p[1] = sm4Sbox((in >> 16) & 0xFF);
    p[2] = sm4Sbox((in >> 8) & 0xFF);
    p[3] = sm4Sbox(in & 0xFF);
    return sm4L(out);
}

// 密钥扩展（使用默认循环，固定密钥）
static void sm4KeyExpand(const uint8_t key[16], uint32_t rk[32]) {
    const uint32_t FK[4] = {0xA3B1BAC6, 0x56AA3350, 0x677D9197, 0xB27022DC};
    const uint32_t CK[32] = {
        0x00070E15, 0x1C232A31, 0x383F464D, 0x545B6269,
        0x70777E85, 0x8C939AA1, 0xA8AFB6BD, 0xC4CBD2D9,
        0xE0E7EEF5, 0xFC030A11, 0x181F262D, 0x343B4249,
        0x50575E65, 0x6C737A81, 0x888F969D, 0xA4ABB2B9,
        0xC0C7CED5, 0xDCE3EAF1, 0xF8FF060D, 0x141B2229,
        0x30373E45, 0x4C535A61, 0x686F767D, 0x848B9299,
        0xA0A7AEB5, 0xBCC3CAD1, 0xD8DFE6ED, 0xF4FB0209,
        0x10171E25, 0x2C333A41, 0x484F565D, 0x646B7279
    };

    uint32_t K[4];
    for (int i = 0; i < 4; i++) {
        K[i] = (key[4*i] << 24) | (key[4*i+1] << 16) | (key[4*i+2] << 8) | key[4*i+3];
        K[i] ^= FK[i];
    }

    for (int i = 0; i < 32; i++) {
        uint32_t tmp = K[1] ^ K[2] ^ K[3] ^ CK[i];
        uint32_t t = sm4L(tmp);  // 加密密钥扩展使用L'，但为了简化直接调用L，实际L'是线性变换，这里相同
        // 实际SM4密钥扩展使用L' = L，所以直接使用sm4L
        rk[i] = K[0] ^ t;
        K[0] = K[1];
        K[1] = K[2];
        K[2] = K[3];
        K[3] = rk[i];
    }
}

// 加密一个块（默认循环版本）
static void sm4EncryptBlock(const uint32_t rk[32], const uint8_t in[16], uint8_t out[16]) {
    uint32_t X[4];
    for (int i = 0; i < 4; i++) {
        X[i] = (in[4*i] << 24) | (in[4*i+1] << 16) | (in[4*i+2] << 8) | in[4*i+3];
    }

    // 32轮循环
    for (int i = 0; i < 32; i++) {
        uint32_t tmp = X[1] ^ X[2] ^ X[3] ^ rk[i];
        uint32_t t = sm4T(tmp);
        uint32_t newX0 = X[0] ^ t;
        X[0] = X[1];
        X[1] = X[2];
        X[2] = X[3];
        X[3] = newX0;
    }

    // 反序输出
    for (int i = 0; i < 4; i++) {
        out[4*i]   = (X[3-i] >> 24) & 0xFF;
        out[4*i+1] = (X[3-i] >> 16) & 0xFF;
        out[4*i+2] = (X[3-i] >> 8) & 0xFF;
        out[4*i+3] = X[3-i] & 0xFF;
    }
}

// 计时和吞吐量测量
static double measure_throughput(void (*encrypt_func)(const uint32_t*, const uint8_t*, uint8_t*),
                                  const uint32_t rk[32], size_t data_size, int repeat) {
    uint8_t *data = malloc(data_size);
    uint8_t *out = malloc(data_size);
    if (!data || !out) {
        free(data); free(out);
        return 0.0;
    }
    memset(data, 0xAA, data_size);  // 固定测试数据

    struct timespec start, end;
    clock_gettime(CLOCK_MONOTONIC, &start);
    for (int r = 0; r < repeat; r++) {
        for (size_t i = 0; i < data_size; i += 16) {
            encrypt_func(rk, data + i, out + i);
        }
    }
    clock_gettime(CLOCK_MONOTONIC, &end);
    free(data); free(out);

    double elapsed = (end.tv_sec - start.tv_sec) + (end.tv_nsec - start.tv_nsec) / 1e9;
    double total_bytes = (double)data_size * repeat;
    return total_bytes / elapsed / (1024 * 1024);  // MB/s
}

int main() {
    // 固定密钥
    uint8_t key[16] = {0x01,0x23,0x45,0x67,0x89,0xab,0xcd,0xef,
                       0xfe,0xdc,0xba,0x98,0x76,0x54,0x32,0x10};
    uint32_t rk[32];
    sm4KeyExpand(key, rk);

    size_t data_size = 1024 * 1024;  // 1MB
    int repeat = 10;                 // 重复10次取平均
    double throughput = measure_throughput(sm4EncryptBlock, rk, data_size, repeat);
    printf("SM4 Default Loop (32 rounds for-loop) Throughput: %.2f MB/s\n", throughput);
    return 0;
}
